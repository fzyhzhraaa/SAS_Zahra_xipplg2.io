<p class="font-weight-bold">SEJARAH PERUMUSAN SUMPAH PEMUDA</p>
<p class="text-justify">Sebelum resmi diterbitkan, naskah Sumpah Pemuda ini memiliki catatan sejarah yang cukup panjang dalam proses perumusannya.

Sumpah Pemuda dilatarbelakangi oleh kondisi Republik Indonesia yang saat itu masih dalam masa penjajahan Belanda.

Para pemuda dari kelompok pelajar di Indonesia sepakat untuk mendirikan organisasi bernama Indonesische Student Bond atau PPPI pada 1926.

Kelompok PPPI ini terdiri dari para pemuda terpelajar yang menempuh pendidikan di Stovia, Technische Hoogeschool te Bandoeng (THS), dan Rechtshogeschool te Batavia (RHS).

Mereka semua mempunyai visi dan misi sama bahwa kawanan penjajah yang mengekang Indonesia bisa disingkirkan dengan kekuatan serta persatuan semangat pemuda bangsa.
<p class="font-weight-bold">KONGRES PEMUDA I
<p class="text-justify">Pada 30 April hingga 2 Mei 1926 Kongres Pemuda mulai gelar di Kawasan Lapangan Banteng, Jakarta, dengan agenda pertemuan kelompok para pemuda Indonesia dalam skala nasional.

Kongres tersebut membahas mengenai strategi hingga gagasan-gagasan pemuda dalam upaya membebaskan diri dari penjajah.
